// Create a program that only prints even values between 0 and 10

public class Even {

    public static void main(String[] args) {

        // Use a for loop to iterate through even values between 0 and 10
        for (int i = 0; i <= 10; i = i + 2) {
            // Print the current value of i (which is even)
            System.out.println(i);
        }
    }
}
