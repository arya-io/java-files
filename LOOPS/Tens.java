// Create a program that counts to 100 by tens.

public class Tens {

    public static void main(String[] args) {

        // Use a for loop to count from 0 to 100 by tens
        for (int i = 0; i <= 100; i += 10) {
            // Print the current value of i
            System.out.println(i);
        }
    }
}
